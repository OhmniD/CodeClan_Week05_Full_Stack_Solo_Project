class Team:
    def __init__(self, name, headquarters, championship_points, engine_supplier, team_colour, logo_url, id = None):
        self.name = name
        self.headquarters = headquarters
        self.championship_points = championship_points
        self.engine_supplier = engine_supplier
        self.team_colour = team_colour
        self.logo_url = logo_url
        self.id = id